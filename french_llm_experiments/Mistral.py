def get_text(row, field):
    value = row.get(field, "")

    if isinstance(value, dict):
        return value.get("transcription_unit", "") or ""

    if isinstance(value, str):
        return value

    return ""

def cer_score(gt, pred):
    if not gt:
        return None
    return Levenshtein.distance(gt, pred) / len(gt)

def evaluate_text_pairs(pairs):
    edits = 0
    chars = 0

    for gt, pred in pairs:
        if not gt:
            continue
        edits += Levenshtein.distance(gt, pred)
        chars += len(gt)

    return {
        "n": len(pairs),
        "CER": edits / chars if chars else None,
        "edits": edits,
        "chars": chars,
    }

def worst_ocr_cases(rows, limit=20):
    cases = []

    for i, row in enumerate(rows):
        gt = get_text(row, "ground_truth")
        ocr = get_text(row, "ocr_hypothesis")

        if not gt:
            continue

        edits = Levenshtein.distance(gt, ocr)

        cases.append({
            "index": i,
            "row": row,
            "baseline_cer": edits / len(gt),
            "edits": edits,
            "chars": len(gt),
        })

    return sorted(cases, key=lambda x: x["baseline_cer"], reverse=True)[:limit]

