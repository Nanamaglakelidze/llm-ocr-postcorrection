def extract_mistral_text(chat_response):
    content = chat_response.choices[0].message.content

    if isinstance(content, str):
        return content.strip()

    pieces = []
    for chunk in content or []:
        chunk_type = getattr(chunk, "type", None)

        # With reasoning_effort="high", ignore thinking chunks and keep final text chunks.
        if chunk_type == "text":
            pieces.append(getattr(chunk, "text", ""))

        elif isinstance(chunk, dict) and chunk.get("type") == "text":
            pieces.append(chunk.get("text", ""))

    return "".join(pieces).strip()

def mistral_correct_ocr(row, examples=None, temperature=0.0): # <-- ტემპერატურა გახდა 0.0
    response = mistral_client.chat.complete(
        model=MISTRAL_MODEL,
        messages=[
            {
                "role": "system",
                "content": "You are an ultra-conservative OCR post-correction system. Respond only with the corrected text, starting immediately with the output."
            },
            {
                "role": "user",
                "content": build_mistral_prompt(row, examples)
            }
        ],
        temperature=temperature,
        max_tokens=4096
    )
    return extract_mistral_text(response)

def safe_mistral_correct_ocr(row, examples=None):
    ocr = get_text(row, "ocr_hypothesis")

    try:
        corrected = mistral_correct_ocr(row, examples)
        error = None
    except Exception as e:
        return ocr, f"api_error: {e}"

    corrected = corrected.strip()

    if not corrected:
        return ocr, "empty_output_fallback"

    # 1. სიგრძის უხეში ფილტრი
    if len(corrected) < len(ocr) * 0.7 or len(corrected) > len(ocr) * 1.3:
        return ocr, "length_fallback"

    return corrected, error

