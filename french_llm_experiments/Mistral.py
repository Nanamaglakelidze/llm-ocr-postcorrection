# 1. ტექსტის ოფიციალური ნორმალიზაცია

def normalize_text(text):
    if not text:
        return ""
    # გადავიყვანოთ პატარა ასოებში
    text = text.lower()
    # სასვენი ნიშნები, არასიტყვიერი სიმბოლოები (\W) და ქვედა ტირე (_) ჩავანაცვლოთ ჰარით
    text = re.sub(r'[\W_]', ' ', text)
    # ზედმეტი ჰარები შევკუმშოთ და ბოლოები ჩამოვაჭრათ
    text = re.sub(r'\s+', ' ', text).strip()
    return text

# 2. ხაზის დონის დეტალური სტატისტიკა (cMER და wMER)

def get_line_stats(gt_text, hyp_text):
    gt_norm = normalize_text(gt_text)
    hyp_norm = normalize_text(hyp_text)

    #  cMER
    if not gt_norm:
        c_num = len(hyp_norm)
        c_den = len(hyp_norm)
        c_mer = 1.0 if hyp_norm else 0.0
    else:
        ops = Levenshtein.editops(gt_norm, hyp_norm)
        I_c = sum(1 for op in ops if op[0] == 'insert')
        c_num = Levenshtein.distance(gt_norm, hyp_norm)  # S + D + I
        c_den = len(gt_norm) + I_c                       # H + S + D + I
        c_mer = c_num / c_den if c_den > 0 else 0.0

    # wMER
    if not gt_norm:
        w_num = len(hyp_norm.split())
        w_den = len(hyp_norm.split())
        w_mer = 1.0 if hyp_norm else 0.0
    else:
        # ვიყენებთ ოფიციალურ jiwer.process_words ფუნქციას
        jiwer_out = jiwer.process_words(gt_norm, hyp_norm)
        w_num = jiwer_out.substitutions + jiwer_out.deletions + jiwer_out.insertions  # S + D + I
        w_den = jiwer_out.hits + jiwer_out.substitutions + jiwer_out.deletions + jiwer_out.insertions  # H + S + D + I
        w_mer = w_num / w_den if w_den > 0 else 0.0

    return {
        "c_num": c_num, "c_den": c_den, "c_mer": c_mer,
        "w_num": w_num, "w_den": w_den, "w_mer": w_mer
    }

