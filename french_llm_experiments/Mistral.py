def find_dynamic_examples(target_row, pool, k=3):
    target_meta = target_row.get("document_metadata", {})
    target_pub = target_meta.get("publication_title", "")
    target_ocr = get_text(target_row, "ocr_hypothesis")

    # 1. გამოვრიცხოთ თავად სატესტო ქეისი (Data Leakage-ის თავიდან ასაცილებლად)
    candidates = [
        row for row in pool
        if get_text(row, "ocr_hypothesis") != target_ocr
        and get_text(row, "ground_truth").strip() != "" # დავრწმუნდეთ, რომ GT არსებობს
    ]

    # 2. ვეცადოთ ვიპოვოთ იგივე გამოცემის (publication_title) ნიმუშები
    same_pub_examples = [
        row for row in candidates
        if row.get("document_metadata", {}).get("publication_title") == target_pub
    ]

    # თუ იგივე გამოცემის საკმარისი ქეისები გვაქვს, ავიღოთ ისინი, თუ არა - შევავსოთ ზოგადი კანდიდატებით
    selected_rows = same_pub_examples[:k]
    if len(selected_rows) < k:
        remaining = k - len(selected_rows)
        # ავიღოთ ისეთი ქეისები, რომლებიც უკვე არ ავირჩიეთ
        used_ocrs = {get_text(r, "ocr_hypothesis") for r in selected_rows}
        extra_examples = [r for r in candidates if get_text(r, "ocr_hypothesis") not in used_ocrs]
        selected_rows.extend(extra_examples[:remaining])

    return selected_rows

def build_mistral_prompt(row, examples=None):
    meta = row.get("document_metadata", {})
    ocr = get_text(row, "ocr_hypothesis")

    few_shot_section = ""
    if examples:
        few_shot_section = "\n### EXAMPLES OF CORRECT CORRECTIONS FROM SIMILAR DOCUMENTS:\n"
        for idx, ex in enumerate(examples, start=1):
            ex_ocr = get_text(ex, "ocr_hypothesis")
            ex_gt = get_text(ex, "ground_truth")
            few_shot_section += f"Example {idx}:\nIn: {ex_ocr}\nOut: {ex_gt}\n\n"

    return f"""
Correct OCR errors in this French historical newspaper text.

CRITICAL RULES:
- The input text is already 95% correct. Do NOT rewrite, do NOT modernize, and do NOT touch words unless they are absolute and obvious OCR noise (like corrupted symbols '@', '#', or broken spaces).
- If a sentence looks historically correct, DO NOT CHANGE A SINGLE CHARACTER. It is better to leave a typo than to change a correct archaic French word.
- Correct OCR errors only.
- Preserve all content and the original order.
- Do not summarize or translate.
- Do not rewrite stylistically.
- Preserve historical French style (e.g., keeping 'oi' instead of 'ai' if present).
- Preserve names, dates, addresses, abbreviations, punctuation, and casing unless clearly caused by OCR.
- Return only the complete corrected text.
- Never return an explanation.
{few_shot_section}
### CURRENT TASK:
Metadata:
language: {meta.get("language")}
document_type: {meta.get("document_type")}
publication_title: {meta.get("publication_title")}

OCR text:
{ocr}
Out:""".strip()

